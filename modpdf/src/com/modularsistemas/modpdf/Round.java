
package com.modularsistemas.modpdf;


public class Round
{

    public Round(double d, int i, int j)
    {
        value = 0.0D;
        exponent = 1;
        num_of_grid_lines = 0;
        value = d;
        exponent = i;
        num_of_grid_lines = j;
    }

    protected double value;
    protected int exponent;
    protected int num_of_grid_lines;
}