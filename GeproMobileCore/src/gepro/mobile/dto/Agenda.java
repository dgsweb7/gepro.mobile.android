package gepro.mobile.dto;

public class Agenda {

	private int id;
	private String titulo;
	private String descricao;
	private String data_inicial;
	private String data_final;

	private String local;
	private String observacao;
	private String hora_inicial;
	private String hora_final;
	private boolean confidencial;
	private boolean particular;
	private boolean horario_ocupado;
	private boolean realizado;
	
	private String Tipo;
	private String PessoaID;
	private String PastaID;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	public String getDataInicial() {
		return data_inicial;
	}
	public void setDataInicial(String data_inicial) {
		this.data_inicial = data_inicial;
	}
	public String getDataFinal() {
		return data_final;
	}
	public void setDataFinal(String data_final) {
		this.data_final = data_final;
	}
	public String getLocal() {
		return local;
	}
	public void setLocal(String local) {
		this.local = local;
	}
	public String getObservacao() {
		return observacao;
	}
	public void setObservacao(String observacao) {
		this.observacao = observacao;
	}
	public String getHoraInicial() {
		return hora_inicial;
	}
	public void setHoraInicial(String hora_inicial) {
		this.hora_inicial = hora_inicial;
	}
	public String getHoraFinal() {
		return hora_final;
	}
	public void setHoraFinal(String hora_final) {
		this.hora_final = hora_final;
	}
	public boolean isConfidencial() {
		return confidencial;
	}
	public void setConfidencial(boolean confidencial) {
		this.confidencial = confidencial;
	}
	public boolean isParticular() {
		return particular;
	}
	public void setParticular(boolean particular) {
		this.particular = particular;
	}
	public boolean isHorario_ocupado() {
		return horario_ocupado;
	}
	public void setHorarioOcupado(boolean horario_ocupado) {
		this.horario_ocupado = horario_ocupado;
	}
	public boolean isRealizado() {
		return realizado;
	}
	public void setRealizado(boolean realizado) {
		this.realizado = realizado;
	}
	public String getTipo() {
		return Tipo;
	}
	public void setTipo(String tipo) {
		Tipo = tipo;
	}
	public String getPessoaID() {
		return PessoaID;
	}
	public void setPessoaID(String pessoaID) {
		PessoaID = pessoaID;
	}
	public String getPastaID() {
		return PastaID;
	}
	public void setPastaID(String pastaID) {
		PastaID = pastaID;
	}


}
