package gepro.mobile.dto;

public class Pasta {
	
	String PastaId;
	String NProcesso;
	String ParteSolicitante;
	String ParteContrario;
	String Area;
	String Titulo;
	String Empresa;
	String DescricaoObjeto;
	String TipoAcao;
	String DataDistribuicao;
	String Foro;
	String Vara;
	
	public String getPastaId() {
		return PastaId;
	}
	public void setPastaId(String pastaId) {
		PastaId = pastaId;
	}
	public String getNProcesso() {
		return NProcesso;
	}
	public void setNProcesso(String nProcesso) {
		NProcesso = nProcesso;
	}
	public String getParteSolicitante() {
		return ParteSolicitante;
	}
	public void setParteSolicitante(String parteSolicitante) {
		ParteSolicitante = parteSolicitante;
	}
	public String getParteContrario() {
		return ParteContrario;
	}
	public void setParteContrario(String parteContrario) {
		ParteContrario = parteContrario;
	}
	public String getArea() {
		return Area;
	}
	public void setArea(String area) {
		Area = area;
	}
	public String getTitulo() {
		return Titulo;
	}
	public void setTitulo(String titulo) {
		Titulo = titulo;
	}
	public String getEmpresa() {
		return Empresa;
	}
	public void setEmpresa(String empresa) {
		Empresa = empresa;
	}
	public String getDescricaoObjeto() {
		return DescricaoObjeto;
	}
	public void setDescricaoObjeto(String descricaoObjeto) {
		DescricaoObjeto = descricaoObjeto;
	}
	public String getTipoAcao() {
		return TipoAcao;
	}
	public void setTipoAcao(String tipoAcao) {
		TipoAcao = tipoAcao;
	}
	public String getDataDistribuicao() {
		return DataDistribuicao;
	}
	public void setDataDistribuicao(String dataDistribuicao) {
		DataDistribuicao = dataDistribuicao;
	}
	public String getForo() {
		return Foro;
	}
	public void setForo(String foro) {
		Foro = foro;
	}
	public String getVara() {
		return Vara;
	}
	public void setVara(String vara) {
		Vara = vara;
	}
	
	
}
